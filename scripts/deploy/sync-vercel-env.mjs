/**
 * Mirror NEXT_PUBLIC_* from repo-root .env into apps/web/.env.production,
 * then optionally commit + push so Vercel auto-redeploys.
 *
 * Standalone usage:
 *   node scripts/deploy/sync-vercel-env.mjs            # write file + git push
 *   node scripts/deploy/sync-vercel-env.mjs --no-push  # write file, skip git
 *
 * Or import { syncVercelEnv } from elsewhere.
 *
 * NEXT_PUBLIC_* is public by design (baked into the client JS bundle), so
 * committing them is safe. We never write secrets here.
 */

import { spawnSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..", "..");

const ROOT_ENV = path.join(repoRoot, ".env");
const WEB_ENV = path.join(repoRoot, "apps", "web", ".env.production");

const PUBLIC_KEYS = [
  "NEXT_PUBLIC_API_URL",
  "NEXT_PUBLIC_CHAIN_ID",
  "NEXT_PUBLIC_RPC_HTTP_URL",
  "NEXT_PUBLIC_BOUNTY_TOKEN_ADDRESS",
  "NEXT_PUBLIC_REAL_V4_HOOK_ADDRESS",
  "NEXT_PUBLIC_BOUNTY_HOOK_ADDRESS",
  "NEXT_PUBLIC_V4_POOL_FEE",
  "NEXT_PUBLIC_V4_TICK_SPACING"
];

function readEnvFile(p) {
  const map = {};
  if (!fs.existsSync(p)) return map;
  for (const line of fs.readFileSync(p, "utf8").split(/\r?\n/)) {
    const m = line.match(/^\s*([A-Za-z_][A-Za-z0-9_]*)\s*=\s*(.*?)\s*$/);
    if (m) map[m[1]] = (m[2] ?? "").replace(/^["']|["']$/g, "");
  }
  return map;
}

/**
 * Write apps/web/.env.production from root .env NEXT_PUBLIC_* values.
 * @returns {{ wrote: boolean, changed: boolean, file: string, content: string }}
 */
export function writeWebEnvProduction() {
  if (!fs.existsSync(ROOT_ENV)) {
    return { wrote: false, changed: false, file: WEB_ENV, content: "" };
  }
  const root = readEnvFile(ROOT_ENV);
  const lines = [
    "# AUTO-GENERATED by scripts/deploy/sync-vercel-env.mjs.",
    "# Do not hand-edit; run `npm run launch` or `npm run launch:wire-pool`.",
    "# Vercel reads this at build time."
  ];
  for (const key of PUBLIC_KEYS) {
    lines.push(`${key}=${root[key] ?? ""}`);
  }
  const content = lines.join("\n") + "\n";

  fs.mkdirSync(path.dirname(WEB_ENV), { recursive: true });
  const prev = fs.existsSync(WEB_ENV) ? fs.readFileSync(WEB_ENV, "utf8") : "";
  if (prev === content) {
    return { wrote: false, changed: false, file: WEB_ENV, content };
  }
  fs.writeFileSync(WEB_ENV, content);
  return { wrote: true, changed: true, file: WEB_ENV, content };
}

function git(args, opts = {}) {
  return spawnSync("git", args, { cwd: repoRoot, encoding: "utf8", ...opts });
}

/**
 * Stage, commit, and push apps/web/.env.production. Touches no other files.
 * Bails (without throwing) on any unexpected git state.
 *
 * @returns {{ ok: boolean, reason?: string, sha?: string }}
 */
export function pushWebEnvProduction(commitMessage) {
  // Verify we're inside a git repo with a remote.
  const inRepo = git(["rev-parse", "--is-inside-work-tree"]);
  if (inRepo.status !== 0) return { ok: false, reason: "not a git repo" };

  // Stage ONLY the web env file. This will not pull in any other changes.
  const add = git(["add", "--", path.relative(repoRoot, WEB_ENV)]);
  if (add.status !== 0) return { ok: false, reason: `git add failed: ${add.stderr.trim()}` };

  // Anything to commit?
  const diff = git(["diff", "--cached", "--name-only"]);
  if (diff.status !== 0) return { ok: false, reason: `git diff failed: ${diff.stderr.trim()}` };
  const staged = diff.stdout.trim().split(/\r?\n/).filter(Boolean);
  if (staged.length === 0) return { ok: true, reason: "no changes" };
  if (staged.length > 1 || !staged[0].endsWith(".env.production")) {
    return { ok: false, reason: `unexpected staged files: ${staged.join(", ")}` };
  }

  const commit = git(["commit", "-m", commitMessage]);
  if (commit.status !== 0) return { ok: false, reason: `git commit failed: ${commit.stderr.trim() || commit.stdout.trim()}` };

  const sha = git(["rev-parse", "--short", "HEAD"]).stdout.trim();

  const push = git(["push"], { stdio: "inherit" });
  if (push.status !== 0) return { ok: false, reason: "git push failed (see output above)", sha };

  return { ok: true, sha };
}

/** Combined helper for callers. */
export function syncVercelEnv({ push = true, commitMessage } = {}) {
  const w = writeWebEnvProduction();
  if (!w.wrote && !w.changed) {
    return { ...w, pushed: false, reason: "no changes" };
  }
  if (!push) return { ...w, pushed: false };
  const msg = commitMessage ?? `chore(web): sync NEXT_PUBLIC_* for Vercel rebuild`;
  const p = pushWebEnvProduction(msg);
  return { ...w, pushed: p.ok, sha: p.sha, reason: p.reason };
}

// CLI entry point. Cross-platform invoked-as-script check.
const isMain = process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href;
if (isMain) {
  const noPush = process.argv.includes("--no-push");
  const result = syncVercelEnv({ push: !noPush });
  if (!result.changed) {
    console.log("· apps/web/.env.production already up to date.");
    process.exit(0);
  }
  console.log(`✓ wrote ${path.relative(repoRoot, result.file)}`);
  if (noPush) {
    console.log("· skipped git push (--no-push). Commit and push when ready to redeploy on Vercel.");
  } else if (result.pushed) {
    console.log(`✓ pushed ${result.sha} → Vercel will redeploy automatically.`);
  } else {
    console.log(`⚠ git push skipped: ${result.reason}`);
    console.log("  Manually:  git add apps/web/.env.production && git commit -m \"sync\" && git push");
  }
}
